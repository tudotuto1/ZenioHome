/* ── i18n engine ─────────────────────────────────────────── */
(function () {
  const LS_KEY = 'zenio_lang';

  function getCurrentLang() {
    return localStorage.getItem(LS_KEY) || 'fr';
  }

  function t(key, ...args) {
    const lang  = getCurrentLang();
    const dict  = window.ZENIO_I18N[lang] || window.ZENIO_I18N.fr;
    const value = dict[key];
    if (typeof value === 'function') return value(...args);
    return value !== undefined ? value : key;
  }

  function applyTranslations() {
    const lang = getCurrentLang();
    document.documentElement.lang = lang;

    // Elements with data-i18n attribute get their textContent replaced
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (el.tagName === 'INPUT' && el.placeholder !== undefined) {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    });

    // Elements with data-i18n-placeholder get placeholder replaced
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-ph'));
    });

    // Update lang toggle button label
    const btn = document.getElementById('langToggle');
    if (btn) btn.textContent = t('lang_btn');

    // Notify other scripts that lang changed
    document.dispatchEvent(new CustomEvent('zenio:langchange', { detail: { lang } }));
  }

  function toggleLang() {
    const next = getCurrentLang() === 'fr' ? 'en' : 'fr';
    localStorage.setItem(LS_KEY, next);
    applyTranslations();
  }

  // Expose globally
  window.ZENIO_T         = t;
  window.ZENIO_LANG      = getCurrentLang;
  window.ZENIO_TOGGLE_LANG = toggleLang;
  window.ZENIO_APPLY_I18N  = applyTranslations;

  // Auto-apply on DOMContentLoaded
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
  } else {
    applyTranslations();
  }
})();
